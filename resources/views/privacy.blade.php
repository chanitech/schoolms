    <p><strong>Effective date:</strong> 14 July 2026</p>

    <p>ShulePRO (also known as SchoolMS) is a school management system operated by
    <strong>Chanitech</strong>, Dar es Salaam, Tanzania. The ShulePRO mobile app gives
    parents and guardians secure access to their children's school information. This
    policy explains what information we handle, how we use it, and the choices you have.
    It applies to the ShulePRO mobile app and the ShulePRO web portal.</p>

    <h2>1. Information we handle</h2>
    <p>ShulePRO processes information on behalf of the school your child attends. This includes:</p>
    <ul>
        <li><strong>Guardian account information</strong> — your name, phone number, and login credentials, used to sign you in and link you to your child.</li>
        <li><strong>Student school records</strong> — your child's enrollment details, class, academic results, and school fee statements, receipts, and payment history. These records are created and managed by the school, not collected by the app.</li>
        <li><strong>Technical information</strong> — basic logs (such as sign-in times and device type) needed to keep the service secure and working.</li>
    </ul>
    <p>The app does not collect your location, contacts, photos, or any other information from your device.</p>

    <h2>2. How we use information</h2>
    <ul>
        <li>To sign you in and show you information about <em>your own children only</em>.</li>
        <li>To display school fees, academic results, and payment receipts issued by the school.</li>
        <li>To keep the service secure, prevent unauthorized access, and fix problems.</li>
    </ul>
    <p>We do <strong>not</strong> sell personal information, show advertising, or share
    personal information with third parties for marketing. Information is only shared
    with the school that manages your child's records, and with service providers that
    host the system securely on our behalf.</p>

    <h2>3. Children's privacy</h2>
    <p>Student records in ShulePRO are provided and managed by schools as part of their
    educational duties. Children do not create accounts in the ShulePRO app; access is
    granted to parents, guardians, and authorized school staff only. Each guardian can
    see only the records of children linked to them by the school.</p>

    <h2>4. Security</h2>
    <p>All communication between the app and our servers is encrypted (HTTPS). Access to
    records is protected by authentication and role-based permissions, and account
    activity is logged.</p>

    <h2>5. Data retention and deletion</h2>
    <p>School records are retained for as long as the school requires them to fulfil its
    educational and legal obligations. If you want your guardian account or its data
    corrected or deleted, contact your school's administration or contact us directly
    using the details below, and we will respond within a reasonable time.</p>

    <h2>6. Changes to this policy</h2>
    <p>If we make significant changes to this policy, we will update this page and revise
    the effective date above.</p>

